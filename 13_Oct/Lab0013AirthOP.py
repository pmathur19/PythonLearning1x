#  Arithmatic operators
'''
+ 	Addition 	x + y
- 	Subtraction 	x - y
* 	Multiplication 	x * y
/ 	Division 	x / y   returns float
% 	Modulus 	x % y   returns remainder
** 	Exponentiation 	x ** y
// 	Floor division 	x // y  returns Quotient
'''


a = int(input("Enter value of a :\n"))
b = int(input("Enter value of b :\n"))

print(a+b)      # 13
print(a-b)      # 7
print(a*b)      # 30
print(a/b)      # 3.33333333333333333
print(a%b)      # 1     (remainder)
print(a**b)     # 10^3 = 1000
print(a//b)     # 3     (quotient)