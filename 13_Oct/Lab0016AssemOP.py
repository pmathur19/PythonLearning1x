# assignment operators
'''
= 	x = 5 	x = 5
+= 	x += 3 	x = x + 3
-= 	x -= 3 	x = x - 3
*= 	x *= 3 	x = x * 3
/= 	x /= 3 	x = x / 3
%= 	x %= 3 	x = x % 3
//= 	x //= 3 	x = x // 3
**= 	x **= 3 	x = x ** 3
&= 	x &= 3 	x = x & 3
|= 	x |= 3 	x = x | 3
^= 	x ^= 3 	x = x ^ 3
>>= 	x >>= 3 	x = x >> 3
<<= 	x <<= 3 	x = x << 3
'''

a = 5
a += 2  # a= a+2
print(a)    # 7

a -= 1  # a=a-1
print(a)    # 6

a *= 3  # a=a*3
print(a)    # 18

a /= 2  # a= a/6
print(a)    # 9.0

a %= 4  # a = a%4
print(a)    # 1.0