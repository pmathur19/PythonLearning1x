# Comparison Operator always retruns boolean value
'''
== 	Equal 	x == y
!= 	Not equal 	x != y
> 	Greater than 	x > y
< 	Less than 	x < y
>= 	Greater than or equal to 	x >= y
<= 	Less than or equal to 	x <= y
'''
x = 5
y = 10
print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)

a = 5
b = 5
print(a >= b)   # True(5>5 or 5=5)