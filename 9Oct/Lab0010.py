# Escape sequence

print("This is a backslash \\")     # This is a backslash \
print("She said: \"Hello, world!\"")        # She said: "Hello, world!"
print("This is a new line \nThis is the second line")       #This is a new line
print("This is a tab\tThis is after the tab")
'''This is the second line
This is a tab	This is after the tab'''